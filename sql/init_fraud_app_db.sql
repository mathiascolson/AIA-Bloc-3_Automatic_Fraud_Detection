-- ============================================================
-- Automatic Fraud Detection - Application Database Initialization
-- ============================================================

-- Ce script initialise les tables applicatives réellement utilisées
-- par les DAGs Airflow et les modules Python du projet.
--
-- Tables principales :
-- - fraud_predictions
-- - fraud_alerts
-- - labeled_transactions
-- - model_cd_decisions
--
-- Les tables sont créées avec CREATE TABLE IF NOT EXISTS afin de ne pas
-- supprimer les données existantes.
--
-- Pour une réinitialisation complète, décommenter les DROP ci-dessous.

-- DROP TABLE IF EXISTS model_cd_decisions CASCADE;
